    fn get_rucksack_groups(rucksacks: Vec<&str>) -> Vec<[&str;3]>{
        let mut outputs: Vec<[&str;3]> = Vec::new();
        let stride_count = rucksacks.len() / 3;
        let mut current_stride: usize = 0;

        while current_stride < stride_count{
            let offset = current_stride * 3;
            outputs.push([rucksacks[offset], rucksacks[offset + 1], rucksacks[offset + 2]]);
            current_stride = current_stride + 1;
        }
        outputs
    }
    
